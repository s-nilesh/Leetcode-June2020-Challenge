#PROBLEM
# Invert a binary tree.

# Example:

# Input:

#      4
#    /   \
#   2     7
#  / \   / \
# 1   3 6   9
# Output:

#      4
#    /   \
#   7     2
#  / \   / \
# 9   6 3   1
# Trivia:
# This problem was inspired by this original tweet[https://twitter.com/mxcl/status/608682016205344768] by Max Howell[https://twitter.com/mxcl]:

